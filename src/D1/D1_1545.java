package D1;

import java.util.Scanner;

public class D1_1545 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int T = sc.nextInt();

        // 0 1 2 3 4 5 6 7 8
        for(int i=T; i>=0; i--) {
            System.out.print(i + " ");
        }
    }
}
